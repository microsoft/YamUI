export enum TextColor {
  /**
   * Primary black text color
   */
  PRIMARY = 'primary',

  /**
   * Secondary dark gray text color
   */
  SECONDARY = 'secondary',

  /**
   * Tertiary color for metadata
   */
  METADATA = 'metadata',

  /**
   * For use on darker backgrounds
   */
  WHITE = 'white',
}

export enum TextSize {
  /**
   * Large headers.
   */
  XXLARGE = 'xxLarge',

  /**
   * Group headers.
   */
  XLARGE = 'xLarge',

  /**
   * Promo headlines.
   */
  LARGE = 'large',

  /**
   * Body text, default size.
   */
  MEDIUM = 'medium',

  /**
   * Secondary text.
   */
  MEDIUM_SUB = 'mediumSub',

  /**
   * Metadata.
   */
  SMALL = 'small',

  /**
   * Very limited use.
   */
  XSMALL = 'xSmall',
}
